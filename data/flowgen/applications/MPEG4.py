# Add Threads to applications
MPEG4.addThread(VU)
MPEG4.addThread(AU)
MPEG4.addThread(MedCPU)
MPEG4.addThread(RAST)
MPEG4.addThread(SDRAM)
MPEG4.addThread(SRAM1)
